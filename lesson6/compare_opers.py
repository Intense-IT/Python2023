# Операторы сравнения 

number = int(input("Введите число "))
print(number == 8) # проверка на равенство
print(number != 5) # проверка на неравенство
print(number > 4) # проверка на больше
print(number < 7) # проверка на меньше
print(number >= 5) # проверка на больше либо равно
print(number <= 9) # проверка на меньше либо равно
